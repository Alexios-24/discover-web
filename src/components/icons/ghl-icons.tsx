// Real GoHighLevel (GHL) icons used by the onboarding flow.
//
// These are inline React components transcribed verbatim from the GHL icon
// library SVGs so they can inherit color via `currentColor` and scale crisply
// like the previous lucide stand-ins. Outline icons come from
// `optimized/24/outline` (and `src/outline`); the two material glyphs (Cars,
// Creative) come from `material/rounded`, matching the Figma onboarding
// designs (file lSuVFjWScTgFMplHt0JsQK). Only the glyph shapes are provided
// here — sizing and color are controlled entirely by the caller.

import type { ReactElement } from "react";

export interface GhlIconProps {
  size?: number;
  className?: string;
}

export type GhlIconComponent = (props: GhlIconProps) => ReactElement;

// 24×24 outline icons: fill none, stroke currentColor, rounded joins. The GHL
// canonical outline set is authored at a 1.5px stroke, which we keep uniform
// across the whole set so the onboarding icons read as one cohesive family.
function StrokeIcon({
  size = 24,
  className,
  d,
}: GhlIconProps & { d: string | string[] }) {
  const paths = Array.isArray(d) ? d : [d];
  return (
    <svg
      aria-hidden="true"
      xmlns="http://www.w3.org/2000/svg"
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={1.5}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
    >
      {paths.map((path, index) => (
        <path key={index} d={path} />
      ))}
    </svg>
  );
}

// Material (rounded, outlined) glyphs: single filled path on the Material
// 0 -960 960 960 viewBox, colored via `fill: currentColor`.
function MaterialIcon({
  size = 24,
  className,
  d,
}: GhlIconProps & { d: string }) {
  return (
    <svg
      aria-hidden="true"
      xmlns="http://www.w3.org/2000/svg"
      width={size}
      height={size}
      viewBox="0 -960 960 960"
      fill="currentColor"
      className={className}
    >
      <path d={d} />
    </svg>
  );
}

